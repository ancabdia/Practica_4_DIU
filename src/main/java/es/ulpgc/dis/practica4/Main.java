    private void lienzo1MouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lienzo1MouseExited
        for (int i = 0; i < 5; i++) {
            lienzo2.lista[i] = null;
        }
        date = new Date();
        historyTextArea.append("[" + dateFormat.format(date) + "]" +
                " Has salido del lienzo\n");
        lienzo2.repaint();
    }//GEN-LAST:event_lienzo1MouseExited

    private void lienzo1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lienzo1MouseMoved
        int x = evt.getX();
        int y = evt.getY();
        
        if(++i == 5){
            i = 0;
        }
        
        lienzo2.addPointList(new Point(x, y), i);
        
        date = new Date();
        historyTextArea.append("[" + dateFormat.format(date) + "]" +
                " Has creado una figura en las coordenadas X: " + x + " Y: " + y + "\n");
        lienzo2.repaint();
    }//GEN-LAST:event_lienzo1MouseMoved

