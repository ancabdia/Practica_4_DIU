    private void pencilColorTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pencilColorTextFieldActionPerformed
        // TODO add your handling code here:
        String s = pencilColorTextField.getText();
        try {
            Color c = ColorFactory.valueOf("rgb(" + s + ")");
            lienzo2.setForeground(c);

            date = new Date();
            historyTextArea.append("[" + dateFormat.format(date) + "]" + 
                    " Has cambiado el color de la estela al color con código RGB: " 
                    + s + "\n");
        } catch (java.lang.IllegalArgumentException ex) {
            lienzo2.setForeground(Color.BLACK);
            JOptionPane.showMessageDialog(null, "RGB not valid, set to default", "Value not valid",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_pencilColorTextFieldActionPerformed

