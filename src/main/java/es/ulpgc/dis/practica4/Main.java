    private void panelColorTextFieldActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_panelColorTextFieldActionPerformed

        String s = panelColorTextField.getText();
        try {
            Color c = ColorFactory.valueOf("rgb(" + s + ")");
            lienzo2.setBackground(c);

            date = new Date();
            historyTextArea.append("[" + dateFormat.format(date) + "]" + 
                    " Has cambiado el color del lienzo al color con código RGB: " + s + "\n");

        } catch (java.lang.IllegalArgumentException ex) {
            lienzo2.setBackground(Color.WHITE);
            JOptionPane.showMessageDialog(null, "RGB not valid, must be the "
                    + "form \"x,x,x\" where \'x\' represents numbers", "Value not valid",
                    JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_panelColorTextFieldActionPerformed

